/** Get icon for a file based on its extension */
export function fileIcon(extension: string, size = 16): string {
  switch (extension.toLowerCase()) {
    case '.md':
    case '.mdx':
      return icon('file-text', size);
    case '.png':
    case '.jpg':
    case '.jpeg':
    case '.gif':
    case '.svg':
    case '.webp':
      return icon('image', size);
    case '.json':
    case '.yaml':
    case '.yml':
      return icon('settings', size);
    case '.js':
    case '.ts':
    case '.jsx':
    case '.tsx':
    case '.css':
    case '.html':
    case '.py':
      return icon('file-code', size);
    case '.excalidraw':
      return icon('image', size);
    default:
      return icon('file', size);
  }
}
