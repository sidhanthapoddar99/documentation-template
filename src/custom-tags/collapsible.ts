/**
 * Create a collapsible tag transformer
 */
export function createCollapsibleTransformer(options: CollapsibleOptions = {}): TagTransformer {
  const { classPrefix = 'collapsible', defaultOpen = false } = options;

  return {
    tag: 'collapsible',
    transform(content: string, attrs: Record<string, string>): string {
      const title = attrs.title || 'Details';
      const isOpen = attrs.open === 'true' || defaultOpen;
      const iconType = attrs.icon || 'default';
      const icon = COLLAPSIBLE_ICONS[iconType] || COLLAPSIBLE_ICONS.default;

      return `
<details class="${classPrefix}"${isOpen ? ' open' : ''}>
  <summary class="${classPrefix}__summary">
    <span class="${classPrefix}__icon">${icon}</span>
    <span class="${classPrefix}__title">${title}</span>
    <span class="${classPrefix}__indicator">
      <svg xmlns="http://www.w3.org/2000/svg" width="16" height="16" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round"><path d="m9 18 6-6-6-6"/></svg>
    </span>
  </summary>
  <div class="${classPrefix}__content">${content}</div>
</details>`.trim();
    },
  };
}

// Default instance
export const collapsibleTransformer = createCollapsibleTransformer();
